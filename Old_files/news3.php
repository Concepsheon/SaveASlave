 <p>Sexual exploitation's victims are always on the minds of the leadership in Belarus. According to official sources, the number of victims 
decreased from around 1000 in 2006 to 81 in 2014. In general, this is a very positive trend, especially when compared to other countries 
in the region, like Ukraine, Russia, Kazakhstan and Moldova, where the trends are less encouraging, according to a 2014 UN report.

</p><p>Belarusian women are the most likely to be exported to Western Europe: Germany, the Netherlands, France, but also as Middle East and 
Russia.Traffickers have become more cautious and do not usually come to Belarus personally, though nine of them were detained inside 
the country in 2014.

</p><p>Modern traffickers have changed their ways: if in the 1990s they attempted to deceive the victims by promising them legal highly paid jobs,
today these schemes no longer work thanks to the spread of communication technology and educational campaigns. They openly invite 
women to work as prostitutes, and the women go in for these dirty jobs fully aware - they simply can earn more doing the same job abroad 
than in Belarus. And many surely hope to find a rich fiancé and start a happier life.

</p><h6>Belarusian Migrants Enslaved in Russia</h6>

<p>Labour slavery has also been on the decline over the past decade, but the major reason for it remains the same. Thousands of 
Belarusians migrate to Russian giant cities of Moscow and Saint Petersburg in search of work. Unemployed men listen to the stories of 
neighbours who have been in Russian and made good money in construction, convincing the to pursue a similar journey. But they often 
find a less optimistic reality awaiting them when they arrive in Russia.
In June 2014 two Belarusians managed to escape from servitude in the Caucasian republic  of Dagestan. Strangers knocked them at a 
Moscow bus station with a spiked drink and brought them to a brick factory. They worked 15 hours a day and were forbidden from leaving 
the factory's territory under threat of a heavy beating.

</p><p>The men managed to run away with the help of the Russian NGO Alternative, who received information about their presence in Dagestan. 
Russian media reported that Dagestan has 600 brick factories, and half of them use slave labour, 10-50 slaves at each factory. And no 
one knows why the Russian government does nothing to stop this outrageous crime.

</p><h6>US Feels Belarus's Efforts Have Been Weak</h6>

<p>Despite the bright official statistics on trafficking from Belarus, the US Department of State has been placing Belarus in its Tier 2 watch list. 
This means that the government is making efforts to comply with the western standards of combating human trafficking, but the total 
number of victims remains significant and shows that the government is failing to resolve the problem.

</p><p>The US report gives several instances where Belarusians are still vulnerable to being compelled to forced labour. It mentions the 
functioning of presidential decree No 9, which forbids leaving one's workplace in any mill from the wood industry without the employer's 
permission. Men who seek jobs abroad remain subject to falling victim to forced labour, as do women via sex trafficking.

</p><p>The report criticises Belarusian officials, who allegedly understate the real number of victims in order to show the government's successful 
performance. The government also shows little interest in cooperating with NGOs who deal with trafficking issues.

</p><h6>New Policies Needed to Fight Trafficking</h6>

<p>While the positive results in the broader anti-trafficking campaign can indeed be observed in Belarus, the government still has a ways to 
go in developing its strategy of dealing with the problem. Belarus succeeded in fighting human trafficking at home, but it still proves itself 
incapable of preventing its citizens from servitude abroad. The authorities should thus focus on problems which do not touch migration 
directly and stem from social and economic conditions in the country.

</p><p>Authorities should specifically target the most vulnerable groups with particular social or regional origin: women from poor or alcoholic 
families and men from the small towns and villages who seek earnings in Russia. The government should develop policies that help these 
groups find a decently paid job at home, and encourage their professional and personal development.</p>
