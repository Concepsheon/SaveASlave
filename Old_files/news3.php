<p>On 6 February a Belarusian businessman received 5 years in prison for enslaving a group of Vietnamese whom he had earlier agreed to 
deliver to the European Union. Meanwhile, the Belarusian government has defined fighting human trafficking as one of its priorities both 
domestically and internationally, where it feels it has been successful.

</p><p>The recent US report on trafficking, however, downgraded Belarus' performance in combating the problem due to its abusive legislation and 
a lack of open access to information on the issue.

</p><p>The positive results from the anti-trafficking campaign are visible in Belarus, though some social groups remain vulnerable to trafficking: 
women from weak families and men from the regions who go to Russia to work. The government needs to develop both employment and 
professional education policies to boost jobs for these groups.

<h6>A Slaveholder from Belarus</h6>

<p>Belarus has not seen slavery for almost two centuries, but recently a rare case of it occurred near Lida, a city in Western Belarus. On 6 
February businessman Siarhej Stoliarau was given five years in prison for organising the illegal migration of individuals. Stoliarau and his 
Russian partners developed a plan to illegally move several Vietnam citizens from Moscow to the European Union. He took 16 of them to a 
truck and brought them to a village in the Lida district.

</p><p>But instead of immediately delivering them to Lithuania, he ordered the Vietnamese to work off the services rendered to him. The migrants 
got accommodation in a shed and had to dig a ditch and do other physical jobs. Stoliarau kept them locked up for eight days, though his 
neighbours had no idea what was happening in the yard next door. The case was revealed only during a border guard check in the village 
for other, unrelated reasons.

</p><h6>A World Anti-Trafficking Activist</h6>

<p>Belarus has engaged in actively combating human trafficking both at home and internationally since 2005. It became one of its major 
foreign policy initiatives, enjoying strong international consensus and a firm backing from many Western countries. The authorities claim 
they brought about a significant drop in trafficking inside Belarus. The following diagram from the Ministry of the Interior review these 
dynamics over the past decade.
